'''Кодирование осуществляется следующим образом:
s = 'aaaabbсaa' преобразуется в 'a4b2с1a2', то есть группы одинаковых
символов исходной строки заменяются на этот символ и количество
его повторений в этой позиции строки.'''
a = input('Enter:')+' '
s = 0
b = a[0]
for i in a:
    if b!= i:
        print(b+str(s), end='')
        s = 0
        b = i
    s+=1
    



